cities = ["Atlanta", "Baltimore", "Chicago", "Denver", "Los Angeles", "Seattle"]

## You can  copy elements  from any number of a main list to a
smaller_list_of_cities = cities[1:]

print(smaller_list_of_cities)


## You can controll the splicing with [1:]  cuts from index one to end or [2:4] or [3;5]
smaller_list_of_cities = cities[1:4]